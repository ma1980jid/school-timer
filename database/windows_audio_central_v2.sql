-- Windows central audio management — version 2 upgrade.
-- Run after database/windows_audio_central.sql.
-- Scope: audio releases, weekly guidance rotation, scheduled campaigns,
-- and the school manager's audio-profile preference.
-- It does not alter timer schedules, viewer data, or school-facing display tables.

begin;

do $$
begin
  if to_regclass('public.windows_audio_releases') is null then
    raise exception 'Run database/windows_audio_central.sql before this v2 upgrade';
  end if;
end;
$$;

